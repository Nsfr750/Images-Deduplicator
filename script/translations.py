def t(key, lang='en', **kwargs):
    """Get translation for key in given language, fallback to English."""
    try:
        # Try to get the translation for the specified language
        translation = TRANSLATIONS.get(lang, {}).get(key, TRANSLATIONS['en'].get(key, key))
        # If the translation is a string, format it with any provided kwargs
        if isinstance(translation, str):
            return translation.format(**kwargs)
        return translation
    except (KeyError, AttributeError):
        # If anything goes wrong, return the key itself
        return key
